#include "libft.h"

int	ft_atoi(const char *s)
{
	int							i;
	int							sign;
	int							res;

	i = 0;
	sign = 1;
	res = 0;
	while ((s[i] >= '\t' && s[i] <= '\r') || s[i] == ' ')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = (s[i] - '0') + (res * 10);
		i++;
	}
	return (res * sign);
}
