#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char res;
	
	res = c;
	if ((res >= '0' && res <= '9') || (res >= 'A' && res <='Z') 
		|| (res >='a' && res <= 'z'))
		return (1);
	return (res);
}
